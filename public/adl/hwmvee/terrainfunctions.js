document.TerrainFunctions = { 
		SampleTerrainByIndex: function(x,y)
        {
            if(!document.terrainHeight) return 0;
            if(x < 0 || y<0 || x>101 || y>101)
                return 0.0;
               
            return document.terrainHeight[(x*101)+y]/85;
        },
        BilinearFilter: function(s1,s2,s3,s4,xfract,yfract)
        {
           var xop = 1 - xfract;
           var yop = 1 - yfract;
           return (s1 * xop + s2 * xfract) * yop + (s3 * xop + s4 * xfract) * yfract;
        },
        SampleNormalByIndex: function(x,y)
        {
            var p1 = [x,y,this.SampleTerrainByIndex(x,y)];
            var p2 = [x+1,y,this.SampleTerrainByIndex(x+1,y)];
            var p3 = [x,y+1,this.SampleTerrainByIndex(x,y+1)];
            
            var v1 = [p2[0] - p1[0],p2[1] - p1[1],p2[2] - p1[2]];
            var v2 = [p3[0] - p1[0],p3[1] - p1[1],p3[2] - p1[2]];
            
            v1 = GLGE.scaleVec3(v1,1/GLGE.lengthVec3(v1));
            v2 = GLGE.scaleVec3(v2,1/GLGE.lengthVec3(v2));
            var norm = GLGE.crossVec3(v1,v2);
            
            return norm;
        },
        normalize: function(v1)
        {
            return GLGE.scaleVec3(v1,1/GLGE.lengthVec3(v1));
        },
        SampleNormalByPos: function(y,x)
        {
           x = x+(101/2);
           y= y+(101/2);
           
           var xfract = x - Math.floor(x);
           var yfract = y - Math.floor(y);
           x = Math.floor(x);
           y = Math.floor(y);
             
           var s1 = this.SampleNormalByIndex(x,y);
           
           var s2 = this.SampleNormalByIndex(x+1,y);
           var s3 = this.SampleNormalByIndex(x,y+1);
           var s4 = this.SampleNormalByIndex(x+1,y+1);
           
           var r1 = this.BilinearFilter(s1[0],s2[0],s3[0],s4[0],xfract,yfract);
           var r2 = this.BilinearFilter(s1[1],s2[1],s3[1],s4[1],xfract,yfract);
           var r3 = this.BilinearFilter(s1[2],s2[2],s3[2],s4[2],xfract,yfract);
           
           return [r2,r1,r3];
        },
        SampleTerrainByPos: function(y,x)
        {
           x = x+(101/2);
           y= y+(101/2);
           
           var xfract = x - Math.floor(x);
           var yfract = y - Math.floor(y);
           x = Math.floor(x);
           y = Math.floor(y);
             
           var s1 = this.SampleTerrainByIndex(x,y);
           var s2 = this.SampleTerrainByIndex(x+1,y);
           var s3 = this.SampleTerrainByIndex(x,y+1);
           var s4 = this.SampleTerrainByIndex(x+1,y+1);
           
           return this.BilinearFilter(s1,s2,s3,s4,xfract,yfract);
           
        },
		GetTerrainData: function()
		{
			var data = { 
				minW: -50.5,
				minH: -50.5,
				
				maxW: 50.5,
				maxH: 50.5,
				maxHeight: 10,
				dw: 1,
				dh: 1,
				sw: 101,
				sh: 101,
				heights: [],
				get_minW: function(){return this.minW;},
				get_minH: function(){return this.minH;},
				get_maxW: function(){return this.maxW;},
				get_maxH: function(){return this.maxH;},
				get_sw: function(){return this.sw;},
				get_sh: function(){return this.sh;},
				get_dw: function(){return this.dw;},
				get_dh: function(){return this.dh;},
				get_heights: function(i,j){return document.terrainHeight[i*101+j]/100;}
			}
		    
			for(var i = 0; i <= 101; i++)
				for(var j = 0; j <= 101; j++)
				    data.heights.push(document.terrainHeight[i*101+j]);
			
            return data;			
		},
		GenerateTerrainPhysics: function()
		{
			return;
				var system=jigLib.PhysicsSystem.getInstance();
                
                var ground=new jigLib.JTerrain(this.GetTerrainData());
                ground.set_friction(0);
				ground.set_mass(0);
				ground._material=new jigLib.MaterialProperties();
				ground._material.set_friction(0);
                system.addBody(ground);
		
		}
	};
	
	
